
"""
    This method convert from byte to MegaByte

    Arguments:
    size:   double  |   Size in b to convert
"""
def b_2_mb(size):
    return (size  / (1024 * 1024))

"""
    This method convert from MegaByte to GigaByte

    Arguments:
    size:   double  |   Size in mb to convert
"""
def mb_2_gb(size):
    return (size  / (1024 * 1024))
